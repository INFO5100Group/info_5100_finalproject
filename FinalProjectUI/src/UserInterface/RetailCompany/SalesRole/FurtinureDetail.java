    private boolean inputValidate() {
        if (jtxName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "furniture name cannot be empty");
            return false;
        }

        if (!jtxPrice.getText().isEmpty()) {
            try {
                Double.parseDouble(jtxPrice.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "price should be numeric");
                return false;
            }
        }

        if (!jtxHeight.getText().isEmpty()) {
            try {
                Double.parseDouble(jtxHeight.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "price should be numeric");
                return false;
            }
        }

        if (!jtxWidth.getText().isEmpty()) {
            try {
                Double.parseDouble(jtxWidth.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "price should be numeric");
                return false;
            }
        }

        if (!jtxLength.getText().isEmpty()) {
            try {
                Double.parseDouble(jtxLength.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "price should be numeric");
                return false;
            }
        }

        return true;
    }

    private void createChooserWindow(JFrame frame) {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.addChoosableFileFilter(new ImgFilter());
        fileChooser.setAcceptAllFileFilterUsed(false);

        int option = fileChooser.showOpenDialog(frame);
        if (option == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            setImg(file);
            try {
                Path temp = Files.move(Paths.get(file.getAbsolutePath()),
                        Paths.get("./image/FurnitureImage/" +file.getName()));
                if (temp != null) {
                    filename = file.getName();
                    System.out.println("File renamed and moved successfully");
                } else {
                    System.out.println("Failed to move the file");
                }
            } catch (IOException ex) {
                Logger.getLogger(FurtinureDetail.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            Picture.setText("");
        }
    }

    /**
     * this one display the image file in internal panel, and set data member to
     * person object
     *
     * @param file image file
     */
    private void setImg(File file) {
        try {
            BufferedImage myImg = ImageIO.read(file);
            Image dImg = myImg.getScaledInstance(200,
                    myImg.getHeight() * 200 / myImg.getWidth(),
                    Image.SCALE_SMOOTH);
            Picture.setIcon(new ImageIcon(dImg));
            Picture.setText("");
        } catch (IOException ex) {
            Picture.setText("no image");
        }
    }
