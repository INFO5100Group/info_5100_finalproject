    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (inputValidate()) {
            furniture.setName(jtxName.getText());
            furniture.setType(jtxType.getText());
            furniture.setWoodType(jtxWood.getText());
            furniture.setColor(jtxColor.getText());
            if (!jtxPrice.getText().isEmpty()) {
                furniture.setPrice(Double.parseDouble(jtxPrice.getText()));
            }
            if (!jtxHeight.getText().isEmpty()) {
                furniture.setHeight(Double.parseDouble(jtxHeight.getText()));
            }
            if (!jtxWidth.getText().isEmpty()) {
                furniture.setWidth(Double.parseDouble(jtxWidth.getText()));
            }
            if (!jtxLength.getText().isEmpty()) {
                furniture.setLength(Double.parseDouble(jtxLength.getText()));
            }
            furniture.setImage(filename);
            DB4OUtil.storeSystem(system);
            JOptionPane.showMessageDialog(null, "furnirure imformation saved");
            last.popupateTable();

            this.dispose();
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

