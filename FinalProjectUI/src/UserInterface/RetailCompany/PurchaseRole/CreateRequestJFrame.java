    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if(inputValidate()){
            WorkRequest wr = new WorkRequest();
            String jsonString = "{"
                    + "\"ProductType\": \"" + jtxWood.getText() + "\","
                    + "\"Quantity\": \"" + jtxQuantity.getText() + "\","
                    + "\"BuyerOffer\": \"" + jtxOffer.getText() + "\","
                    + "\"SellerOffer\": \"\","
                    + "\"Seller\": {}"
                    + "}";
            wr.setMessage(jsonString);
            wr.setSender(account);
            wr.setStatus("Buyer submit request");
            wr.getReceivers().put(new Account(), true);

            // add request to queue
            try {
                system.getWorkQueue().addRequest(wr);
            } catch (Exception ex) {
                Logger.getLogger(RegistForEnterpriseJPanel.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex);
                return;
            }

            JOptionPane.showMessageDialog(null, " Your repuest is submited, please wait for furniture manufacturer accept your request");
            DB4OUtil.storeSystem(system);
            last.populateTable();
            this.dispose();
            this.setVisible(false);
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

