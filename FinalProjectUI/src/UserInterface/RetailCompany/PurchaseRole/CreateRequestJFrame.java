     public boolean inputValidate(){
        if(jtxOffer.getText().isEmpty() || jtxQuantity.getText().isEmpty() 
                || jtxWood.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill all text fields");
            return false;
        }
        
        try{
            Double.parseDouble(jtxQuantity.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "quantity should be numeric");
            return false;
        }
        
        try{
            Double.parseDouble(jtxOffer.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "your offer should be numeric");
            return false;
        }
        
        return true;
    }
    
