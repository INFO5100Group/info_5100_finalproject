    public void populateInfo() {
        txtName.setText(currFurniture.getName());
        txtPrice.setText("$ " + df2.format(currFurniture.getPrice()));
        txtQuantity.setText(quantity + "");
        txtTotal.setText("$ " + df2.format(currFurniture.getPrice() * quantity));
        String address = account.getPerson().getFirstName() + " " + account.getPerson().getLastName() + "\n"
                + account.getPerson().getState() + "\n"
                + account.getPerson().getStreet() + "\n"
                + account.getPerson().getZipCode();
        txtAddress.setText(address);
        try {
            File imgFile = new File("./image/FurnitureImage/" + currFurniture.getImage());
            BufferedImage myImg = ImageIO.read(imgFile);
            Image dImg = myImg.getScaledInstance(300,
                    myImg.getHeight() * 300 / myImg.getWidth(),
                    Image.SCALE_SMOOTH);
            lblImage.setIcon(new ImageIcon(dImg));
            lblImage.setText("");
        } catch (Exception e) {
            lblImage.setText("No Image");
        }

    }

