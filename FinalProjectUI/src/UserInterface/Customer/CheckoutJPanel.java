    private void btnBuyMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuyMousePressed
        WorkRequest wr = new WorkRequest();
        wr.getReceivers().put(account, true);
        try {
            wr.setSender(system.getEnterprises().getEnterpriseByName(currFurniture.getSeller()).getAdmin());
        } catch (Exception e) {
            wr.setSender(system.getEnterprises().getEnterpriseByName("Furtinure Dean").getAdmin());
        }
        wr.setStatus("Customer placed an order");
        JSONObject currInfo = new JSONObject();
        currInfo.put("furnitureID", currFurniture.getID());
        currInfo.put("Product", currFurniture.getName());
        currInfo.put("quantity", quantity);
        wr.setMessage(currInfo.toString());
        try {
            system.getWorkQueue().addRequest(wr);
        } catch (Exception ex) {
            Logger.getLogger(CheckoutJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        realFurnitures.removeFurniture(currFurniture, quantity);
        DB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "you have place an order for " + currFurniture.getName());
        CardLayoutNavigator.goBack(contianer, this);
    }//GEN-LAST:event_btnBuyMousePressed


