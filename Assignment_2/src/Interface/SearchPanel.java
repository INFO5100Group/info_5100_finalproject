    boolean checkValidation(){
        if(!"".equals(minSeat) && !"".equals(maxSeat)){
            try{
                int testMin = Integer.parseInt(minSeat);
                int testMax = Integer.parseInt(minSeat);
                if(testMax < testMin){
                    javax.swing.JOptionPane.showMessageDialog(null, 
                        "Min Seat Num cannot larger than Max Seat Num");
                    return false;
                }
            } catch(NumberFormatException e){
                javax.swing.JOptionPane.showMessageDialog(null, 
                    "wrong format for Min or Max Seat Num(numeric)");  
                return false;
            }
        }else if(!"".equals(minSeat) || !"".equals(maxSeat)){
            javax.swing.JOptionPane.showMessageDialog(null, 
                        "Please enter both Min and Max Seat Num");
            return false;
        }
        
        if(!"".equals(year)){
            try{
                int testYear = Integer.parseInt(year);
            } catch(NumberFormatException e){
                javax.swing.JOptionPane.showMessageDialog(null, 
                    "wrong format for Year (numeric)");  
                return false;
            }
        }
        return true;
    }
    
