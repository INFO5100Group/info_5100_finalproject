    private boolean checkValidate(){
        if(txtSerial.getText().equals("")
                || txtLocation.getText().equals("")
                || txtMaker.getText().equals("")
                || txtModel.getText().equals("")
                || txtYear.getText().equals("")
                || txtSeats.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, 
                        "Text Field Cannot Be Empty");
            return false;
        }
        
        try{
            int test = Integer.parseInt(txtYear.getText());            
        }catch(NumberFormatException e){
            javax.swing.JOptionPane.showMessageDialog(null, 
                    "wrong format for Year (numeric)");  
                return false;
        }
        
        try{
            int test = Integer.parseInt(txtSeats.getText());            
        }catch(NumberFormatException e){
            javax.swing.JOptionPane.showMessageDialog(null, 
                    "wrong format for seats number (numeric)");  
                return false;
        }
            
        return true;
    } 
    
    private void saveCar() {
        car = carlist.addCar();
        car.setSeriesNum(txtSerial.getText());
        car.setCity(txtLocation.getText());
        car.setMaker(txtMaker.getText());
        car.setIsAvaliable(checkAvaliable.isSelected());
        car.setModel(txtModel.getText());
        car.setUpdateTime(new Date());
        car.setSeatNum(Integer.parseInt(txtSeats.getText()));
        car.setManufactureYear(Integer.parseInt(txtYear.getText()));
        Date expiredDate = new Date(Integer.parseInt(boxExpiredYear.getSelectedItem().toString()) - 1900
                                    ,Integer.parseInt(boxExpiredMonth.getSelectedItem().toString()) -1
                                    ,Integer.parseInt(boxExpiredDay.getSelectedItem().toString()));
        car.setMaintanceExpiredDate(expiredDate); 
        javax.swing.JOptionPane.showMessageDialog(null, 
                        "car " + car.getSeriesNum() + " created");
        System.out.println("create:" + car);
        this.removeAll();
        this.revalidate();
        this.repaint();
    }

